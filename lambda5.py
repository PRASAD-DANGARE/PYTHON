# Python Program A Lambda Function That Returns Square Of Elements In A List Using map() Function

'''
Function Name    :  Square Of Elements In A List Using Map(). 
Function Date    :  9 Sep 2020
Function Author  :  Prasad Dangare
Input            :  Integer
Output           :  Integer
'''

lst = [1,2,3,4,5]
lst1 = list(map(lambda x: x*x, lst))
print(lst1) 